public class OperacaoBasica{
    public static void main (string [] args){
        int A = 10;
        int B = 5;
        system.out.println("soma:" +(A+B));
        system.out.println("subtração:" +(A-B));
        system.out.println("multiplicação:" +(A*B));
        system.out.println("divisão:" +(A/B));
    }
}